package ex01;

public class Main {
	//김주선
	public static void main(String[] args) {
		//학급 생성
		School school = new School();
		
		//점수 배열
		int[] grades1 = {90,80,80};
		int[] grades2 = {90,80,70};
		int[] grades3 = {90,90,60};
		
		//학생 생성
		Student st1 = new Student("김주선", grades1);
		Student st2 = new Student("김모모", grades2);
		Student st3 = new Student("김마마", grades3);
		
		//학생 등록 메소드
		school.registerStudent(st1);
		school.registerStudent(st2);
		school.registerStudent(st3);
		

		
		//학생 목록보기
		school.showStudentList();
		//학생 평균보기
		school.showScoreAvg();
		
	}
}
