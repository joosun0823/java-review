package variable;

public class Variable03 {
	public static void main(String[] args) {
		String name = "김주선";
		int age = 25;
		
		System.out.println(name);
		System.out.println(age);
		
		// 제 이름은 김주선이고 제 나이는 25살입니다
		System.out.println("제 이름은 " + name + "이고 "
	            + "제 나이는 " + age + "살입니다");
	}
}
